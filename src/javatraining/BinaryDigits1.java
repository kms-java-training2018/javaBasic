package javatraining;

public class BinaryDigits1 {

	public static void main(String[] args) {
		// 配列の宣言
		int binaryDigits[] = new int[5];
		binaryDigits[0] = 1;
		binaryDigits[1] = 2;
		binaryDigits[2] = 4;
		binaryDigits[3] = 8;
		binaryDigits[4] = 16;
		
		// 3つ目の値4を出力
		System.out.println("3つ目の値は" + binaryDigits[2] + "です。");
	}
}
